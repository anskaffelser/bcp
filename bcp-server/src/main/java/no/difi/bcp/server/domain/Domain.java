package no.difi.bcp.server.domain;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import java.io.Serializable;
import java.util.UUID;

/**
 * @author erlend
 */
@Entity
public class Domain implements Serializable {

    private static final long serialVersionUID = 2641860749881854954L;

    @Id
    @GeneratedValue(strategy = GenerationType.AUTO)
    private long id;

    private String identifier;

    private String title;

    public static Domain newInstance() {
        Domain domain = new Domain();
        domain.setIdentifier(UUID.randomUUID().toString());

        return domain;
    }

    public static Domain newInstance(String title) {
        Domain domain = newInstance();
        domain.setTitle(title);

        return domain;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getIdentifier() {
        return identifier;
    }

    public void setIdentifier(String identifier) {
        this.identifier = identifier;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

}
